            #####################################################
            ###############   PLOT CURRENT FIT  #################
            #####################################################

            # Plot the spectrum and the fit, updated continuously

            # Dictionnary with the results of the last fit iteration
            dparams = {}
            for name in dparams_list:
                dparams[name[:-5]] = dparams_list[name][-1]
